    <script>
        function modal_kandidat(id) {
            $.ajax({
                url: "<?php echo site_url('home/show_personal_kandidat/') ?>" + id,
                type: "GET",
                dataType: "JSON",
                success: function(data) {
                    $("#img").attr("src", '<?php echo base_url('public/kandidat/') ?>' + data.kandidat_photo);
                    $("#visi").html(data.kandidat_visi);
                    $("#misi").html(data.kandidat_misi);
                    $("#program").html(data.kandidat_program);
                    $("#vidio").html('<div class="embed-responsive embed-responsive-16by9"><iframe src="'+data.kandidat_video+'" title="YouTube video player" frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture" allowfullscreen></iframe></div>');
                    $("#lain").html(data.kandidat_lain);
                    $('#modal_candidat').modal('show');
                    $('.modal-title').text(data.kandidat_nama);
                },
                error: function(jqXHR, textStatus, errorThrown) {
                    alert('Error get data from ajax');
                }
            });
        }
    </script>
